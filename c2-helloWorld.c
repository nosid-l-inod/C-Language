#include <stdio.h>

int main() {

    // Single line comment

    /*
        This is 
        a multiline
        comment
    */

    // printf - mostrar mensagem
    printf("Hello world\n");

    // \n - line break
    printf("i'm learning C\n");


    // Escape sequence

    // Tab - tab space
    printf("1\t2\t3\n");

    // Display quotes
    printf("\"I like pizza\"");

    return 0;
}